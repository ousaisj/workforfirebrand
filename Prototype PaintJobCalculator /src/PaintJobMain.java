    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
String str = "Your paint area is calculated to be "+ roomArea+" square metres"+" \n";
         
Double undercoatCost  = 0.0;
     if (jRadioButton1.isSelected()) 
    { 
       undercoatCost = 0.5;
       
       str += "The undercoat cost will be £"+ roomArea* undercoatCost+" \n";
       
      } else {
         
       undercoatCost = 0.0;
       
        str += "The undercoat cost will be £0 \n";
        
     }
     
      Double paintTypeCost = 0.0;
        
        
       String paintType = jComboBox1.getSelectedItem().toString();
     
      if(paintType == "STANDARD") {
       
       paintTypeCost = 1.00;
       
       str += "For standard quality paint it will cost and additional £"+ roomArea* paintTypeCost+" \n";
       
          } else if (paintType == "ECONOMY"){
              
       paintTypeCost = 0.45;
       
       str += "For economy quality paint it will cost and additional £"+ roomArea* paintTypeCost+" \n";
              
          }
       else if (paintType == "LUXURY"){ 
           
       paintTypeCost = 1.75 ;
       
       str += "For Luxury quality paint it will cost and additional £"+ roomArea* paintTypeCost+"\n";
          
          }
     
      if (undercoatCost == 0.0){
            
             totalCost = (roomArea * paintTypeCost); 
             
            str +=   "Your overall total for this transaction will be £"+ roomArea * paintTypeCost+" \n";
        }
        else {
             totalCost = (roomArea * undercoatCost) + (roomArea * paintTypeCost); 
             
            str += "Your overall Total for this transaction will be £"+ ((roomArea * undercoatCost) + (roomArea * paintTypeCost))+" \n";
        }
        
     
     invoiceStr = str;
         
       jOptionPane2.showMessageDialog(null, str);  // TODO add your handling code here:
    



    }//GEN-LAST:event_jButton3ActionPerformed

