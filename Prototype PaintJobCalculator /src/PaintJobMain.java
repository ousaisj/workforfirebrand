    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      File file = new File("SavedInvoice.txt");

        try {
            // creates the file
            file.createNewFile();
            
            
      // creates a FileWriter Object
      FileWriter writer = new FileWriter(file); 
      
      // Writes the content to the file
      writer.write(invoiceStr); 
      writer.flush();
      writer.close();

      // Creates a FileReader Object
      FileReader fr = new FileReader(file); 
      char [] a = new char[50];
      fr.read(a);   // reads the content to the array
      
      for(char c : a)
         System.out.print(c);   // prints the characters one by one
      fr.close();        // TODO add your handling code here:
      
      
        } catch (IOException ex) {
            Logger.getLogger(PaintJobMain.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTextField2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyPressed
        // TODO add your handling code here:
        int key=evt.getKeyCode();
        if((key>evt.VK_0&&key<=evt.VK_9)||(key>=evt.VK_NUMPAD0&&key<=evt.VK_NUMPAD9)||key==KeyEvent.VK_BACKSPACE)
        {
           jTextField2.setEditable(true);
           jTextField2.setBackground(Color.yellow); 
        }
        else
        {
        
          jTextField2.setEditable(false);
          jTextField2.setBackground(Color.red);
        }
    }//GEN-LAST:event_jTextField2KeyPressed

